    void animation_phase(void) {
        current_anim_frame = (current_anim_frame + 1) % ANIM_FRAMES;
        
        switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_raw_P(slow[abs((ANIM_FRAMES - 1) - current_anim_frame)], ANIM_SIZE);
            break;
        case 1:
            oled_write_raw_P(fast[abs((ANIM_FRAMES - 1) - current_anim_frame)], ANIM_SIZE);
            break;
        }
    }

    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
        anim_timer = timer_read32();
        animation_phase();
    }

    oled_set_cursor(0, 6);
    oled_write_P(PSTR("Puca\nPad\n"), false);
    oled_write_P(PSTR("-----\n"), false);
    // Host Keyboard Layer Status
    oled_write_P(PSTR("Mode\n"), false);
    oled_write_P(PSTR("\n"), false);
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_P(PSTR("Base\n"), false);
            break;
        case 1:
            oled_write_P(PSTR("Other\n"), false);
            break;
    }
    return true;

#include "puca.h"

bool process_record_kb(uint16_t keycode, keyrecord_t* record) {
    if (!process_record_user(keycode, record)) {
        return false;
    }
    switch (keycode) {
        case MC_00:
            if (record->event.pressed) {
                SEND_STRING("00");
            }
            break;
    }
    return true;
}

bool encoder_update_kb(uint8_t index, bool clockwise) {
    if (!encoder_update_user(index, clockwise)) { return false; }

    if (clockwise) {
        tap_code_delay(KC_VOLU, 10);  // Right
    } else {
        tap_code_delay(KC_VOLD, 10);  // Left
    }

    return false;
}

#define RGB_LAYER0 0x00, 0x48, 0xAA
#define RGB_LAYER1 0xFF, 0x55, 0x00

//This will run every time the layer is updated
uint8_t currentLayer;

layer_state_t layer_state_set_user(layer_state_t state) { 
    currentLayer = get_highest_layer(state);

    for (int iii = 0; iii <= RGBLED_NUM; iii++)  {
        switch (currentLayer) {
            case 0:
                setrgb(RGB_LAYER0, &led[iii]);
                break;
            case 1:
                setrgb(RGB_LAYER1, &led[iii]);
                break;
        }
    }

    rgblight_set();

    return state;
}

